enum {r0, r1, r2 ,r3, r12, lr, pc, psr};
void hard_fault_handler_c(uint32_t * hardfault_args)
{
	unsigned int stacked_r0;
	unsigned int stacked_r1;
	unsigned int stacked_r2;
	unsigned int stacked_r3;
	unsigned int stacked_r12;
	unsigned int stacked_lr;
	unsigned int stacked_pc;
	unsigned int stacked_psr;

	stacked_r0 = ((unsigned long) hardfault_args[0]);
	stacked_r1 = ((unsigned long) hardfault_args[1]);
	stacked_r2 = ((unsigned long) hardfault_args[2]);
	stacked_r3 = ((unsigned long) hardfault_args[3]);

	stacked_r12 = ((unsigned long) hardfault_args[4]);
	stacked_lr = ((unsigned long) hardfault_args[5]);
	stacked_pc = ((unsigned long) hardfault_args[6]);
	stacked_psr = ((unsigned long) hardfault_args[7]);

	printf ("\n\n[Hard fault handler - all numbers in hex]\n");
	printf ("R0 = 0x%08x\n", stacked_r0);
	printf ("R1 = 0x%08x\n", stacked_r1);
	printf ("R2 = 0x%08x\n", stacked_r2);
	printf ("R3 = 0x%08x\n", stacked_r3);
	printf ("R12 = 0x%08x\n", stacked_r12);
	printf ("LR [R14] = 0x%08x  subroutine call return address\n", stacked_lr);
	printf ("PC [R15] = 0x%08x  program counter\n", stacked_pc);
	printf ("PSR = 0x%08x\n", stacked_psr);
	printf ("BFAR = 0x%08x\n", (*((volatile unsigned int *)(0xE000ED38))));
	printf ("CFSR = 0x%08x\n", (*((volatile unsigned int *)(0xE000ED28))));
	printf ("BFSR = 0x%08x\n", (*((volatile unsigned int *)(0xE000ED29))));
	printf ("HFSR = 0x%08x\n", (*((volatile unsigned int *)(0xE000ED2C))));
	printf ("DFSR = 0x%08x\n", (*((volatile unsigned int *)(0xE000ED30))));
	printf ("AFSR = 0x%08x\n", (*((volatile unsigned int *)(0xE000ED3C))));
	printf ("SCB_SHCSR = 0x%08x\n", (unsigned int)SCB->SHCSR);
	while (1);
}
